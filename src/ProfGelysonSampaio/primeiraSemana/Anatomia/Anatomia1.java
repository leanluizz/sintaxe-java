package ProfGelysonSampaio.primeiraSemana.Anatomia;
public class Anatomia1 {
	public static void main(String[] args) {
		
		// Tipos de variáveis não podem ser violadas por exemplo um tipo int que significa inteiro não pode ser uma string;
		
		boolean Verdade = true;
		double Decimal = 2.2;
		int A = 2;
		int Ano = 2023;
		String BR = "Brasil";
		System.out.println(BR + Ano);
		System.out.println(Decimal + "." + A + !Verdade);
	}
}

